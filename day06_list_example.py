# 리스트 예제: 여러 값을 하나에 담기

fruits = ["사과", "바나나", "체리"]  # 리스트 생성
print(fruits)                       # 리스트 전체 출력
print(fruits[0])                    # 첫 번째 아이템 출력 ("사과")
print(fruits[-1])                   # 마지막 아이템 출력 ("체리")

# 리스트에 새로운 아이템 추가
fruits.append("오렌지")
print(fruits)
